    private void textAñoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textAñoKeyTyped
        
        char car = evt.getKeyChar();
        String a = textAño.getText();
       
        
        if(a.length()>=4)
        {
            evt.consume();
        }
        else
        {
            if( (car<'0' || car>'9'))
                {
                    evt.consume();
                }
            
            else
            {
                int año =0;
            
            if(a.length()>0)
                 año =Integer.parseInt(a+car);
            
            if(año>2014)
            {
                evt.consume();
            }
            
            }
        }
        
    }//GEN-LAST:event_textAñoKeyTyped

