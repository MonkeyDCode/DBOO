    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = textNombre.getText();
        String director = textDirector.getText();
        String año = textAño.getText();
        String cantidad = textCantidad.getText();
        String genero = (String) comboGenero.getSelectedItem();
        boolean flag=VideosBD.isPelicula(nombre);
        
        
        if(flag)
        {
            JOptionPane.showMessageDialog(this, "Pelicula existente");
        }
        else
        {
            if(!nombre.equals("")
                    && !director.equals("")
                    && !año.equals("") && año.length()==4
                    && !cantidad.equals("")
                    && comboGenero.getSelectedIndex()>0)
            {

                VideosBD.alta(nombre, director, año, genero, cantidad);
                JOptionPane.showMessageDialog(this,"Pelicula agregada exitosamente");
                textNombre.setText("");
                textDirector.setText("");
                textAño.setText("");
                textCantidad.setText("");
                comboGenero.setSelectedIndex(0);

            }
            else
            {
                JOptionPane.showMessageDialog(this,"Revisa todos los campos");
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

